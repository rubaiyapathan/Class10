VC = input('Enter the letter : ')
match VC:
    case "a":
        print(VC, 'is a vowel')
    case "e":
        print(VC, 'is a vowel')
    case "i":
        print(VC, 'is a vowel')
    case "o":
        print(VC,'is a vowel')
    case "u" :
        print(VC,'is a vowel')
    case _:
        print(VC,'is a consonant')
